package contracts

// CopyEnv returns a shallow copy of the given environment map.
// Returns nil when the input is empty or nil.
func CopyEnv(env map[string]string) map[string]string {
	if len(env) == 0 {
		return nil
	}
	out := make(map[string]string, len(env))
	for k, v := range env {
		out[k] = v
	}
	return out
}

// MergeEnv returns a new map containing all entries from base with override
// entries applied on top. Returns nil when both inputs are empty or nil.
func MergeEnv(base, override map[string]string) map[string]string {
	if len(base) == 0 && len(override) == 0 {
		return nil
	}
	out := make(map[string]string, len(base)+len(override))
	for k, v := range base {
		out[k] = v
	}
	for k, v := range override {
		out[k] = v
	}
	return out
}
